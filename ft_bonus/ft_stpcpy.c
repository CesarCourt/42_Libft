char			*ft_stpcpy(char *dst, char const *src)
{
	int i;

	i = 0;
	while (src && src[i] && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (&dst[i]);
}
